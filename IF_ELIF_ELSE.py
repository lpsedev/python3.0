
# EL, ELIF, ELSE

if True:
    print('Verdadeiro') # é necessário está dentro da hierarquia.
    # É necessário que para que os códigos sejam executados, eles precisam
    # está dentro desse limite.
    num_1 = 2
    num_2 = 4
    print(num_1 + num_2)

    # Caso essa expressão nã oseja verdadeira
    # é só colocar um print fora do IF.
print('Não é verdadeira') # Nesse caso essa linha será executada.

# Outra expressão seria:
if True:
    print('Luiz')
else:               # Pode ser usado tb o  ELIF(else if)
    print('Lycia')
